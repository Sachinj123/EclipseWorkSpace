// UPCASTING AND DOWNCASTING
public class Program2 {
	
	//DOWNCASTING DOUBLE TO INT
	public static void main(String[] args) {
		double num1=10.5;
		int num2=(int) num1;
		System.out.println("NUMBER2 ->"+num2);
		
	}

	public static void main1(String[] args) {
		
		//UPCASTING INT TO DOUBLE 
		int num1=10;
		double num2=num1;
		System.out.println("NUMBER2 ->"+num2);

	}

}
