
public class IntToLong {
	
	public static void main(String[] args) {
		long l=4789;
		int num1=(int) l;
		System.out.println("AFTER CONVERSION OF LONG TO INT:"+num1);
	}

	public static void main1(String[] args) {
		// INT -> LONG IS UPCASTING
		
		int num1=45;
		long l=num1;
		System.out.println("AFTER CONVERSION INT TO LONG:"+l);

	}

}
