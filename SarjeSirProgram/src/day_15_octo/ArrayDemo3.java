package day_15_octo;

public class ArrayDemo3 {
	
	public static void main(String[] args) {
		
		int [][] ar = new int [2][];
		
		int [][] ar1 = new int [2][3];
		
		for(int a []: ar1) {
			System.out.println(a);
		}
		System.out.println("===================================");
		for(int a[] : ar) {
			System.out.println(a);
		}
	}

}
